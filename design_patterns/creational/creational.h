/**********************************************************************************************************************
+=============================================== Creational Design Patterns ==========================================+
|                                                                                                                     |
| Description:                                                                                                        |
| Creational design patterns deal with object creation mechanisms, aiming to create objects in a manner suitable to   |
| the situation. They help make a system independent of how its objects are created, composed, and represented.       |
| These patterns focus on providing flexibility and reusability by abstracting the instantiation process.             |
|                                                                                                                     |
| Key Benefits:                                                                                                       |
| - Promote code flexibility by decoupling the creation logic from the usage of objects.                              |
| - Enhance scalability by making it easy to add new types of objects without changing existing code.                 |
| - Support better system design by managing complex object creation processes.                                       |
|                                                                                                                     |
+=====================================================================================================================+
***********************************************************************************************************************/


